package facilityMaintanence;

public class Cost {
	private int costID;
	private int laborCost;
	private int materialCost;
	private int extra;

	public int getCostID() {
		return costID;
	}

	public int getLaborCost() {
		return laborCost;
	}

	public int getMaterialCost() {
		return materialCost;
	}

	public int getExtra() {
		return extra;
	}
}
